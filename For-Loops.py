# we can loop through even a string as they have a sequence of indexing
for x in "heera":
    print(x) 

# using the break in the for loop
fruits = ["apple","banana","cherry","watermelon","orange"]
for x in fruits:
    print(x)
    if x == "watermelon":
        break
